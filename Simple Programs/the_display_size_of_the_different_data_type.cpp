//The Display Size of the Different Data Type

#include <iostream>
using namespace std;

int main() 
{
	cout << "Size of int in this system is: " << sizeof(int) << endl;
	cout << "Size of float in this system is: " << sizeof(float) << endl;
	cout << "Size of char in this system is: " << sizeof(char) << endl;
	cout << "Size of double in this system is: " << sizeof(double) << endl;
	cout << "Size of bool in this system is: " << sizeof(bool) << endl;
	cout << "Size of string in this system is: " << sizeof(string) << endl;
	return 0;
	}